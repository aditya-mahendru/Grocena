package com.example.javagrocena.models;

public class SearchModel {

    String id;
    String name;
    int image;
    String status;
    int price;

    public SearchModel(String id, String name, int image, String status, int price) {
        this.id = id;
        this.name = name;
        this.image = image;
        this.status = status;
        this.price = price;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getImage() {
        return image;
    }

    public void setImage(int image) {
        this.image = image;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public int getPrice() {
        return price;
    }

    public void setPrice(int price) {
        this.price = price;
    }
}